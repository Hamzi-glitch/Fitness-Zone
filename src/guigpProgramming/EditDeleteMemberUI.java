    private double getRate() {
    boolean student = studentRB.isSelected();
    return switch (membershipCB.getSelectedItem().toString()) {
        case "Basic" -> student ? 80 : 120;
        case "Premium" -> student ? 150 : 200;
        case "VIP" -> student ? 280 : 350;
        default -> 0;
    };
}

